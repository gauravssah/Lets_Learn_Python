# dataTypes

a = 1   # int
b = 3   # int
c = 1.0 # float
d = 3.0 # float

print(a+b+c+d) # 8.0

# string
a = "hello"
b = "world"

print(a+b)      # helloworld 

# boolean
a = True
b = False

print(a+b)      # 1 

# list
a = [1,2,3]
b = [4,5,6]

print(a+b)      # [1,2,3,4,5,6]

# tuple
a = (1,2,3)
b = (4,5,6)

print(a+b)      # (1,2,3,4,5,6) 

# dictionary
a = {"a":1,"b":2}
b = {"c":3,"d":4}

print(a+b)      # {'a': 1, 'b': 2, 'c': 3, 'd': 4}      

# set
a = {1,2,3}
b = {4,5,6}

print(a+b)      # {1,2,3,4,5,6}

