a = 5
b = type(a)
print(b)  # <class 'int'>

a = 5.5
b = type(a)
print(b) # <class 'float'>

a = "Gaurav"
b = type(a)
print(b) # <class 'str'>

a = True
b = type(a)
print(b) # <class 'bool'>

# typecasting

print("Typecasting")

a = "10"
b = int(a)
print(b) # 10
print(type(b)) # <class 'int'>

a = 10
b = str(a)
print(b) # 10
print(type(b)) # <class 'str'>  

a = 10
b = float(a)
print(b) # 10.0
print(type(b)) # <class 'float'>






